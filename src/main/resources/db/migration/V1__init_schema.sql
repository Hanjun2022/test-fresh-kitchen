CREATE TABLE ingredient (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    storage_id BIGINT NOT NULL,
    catalog_id BIGINT NULL,
    name VARCHAR(100) NOT NULL,
    registered_at DATE NULL,
    expires_at DATE NULL,
    expiry_source_type VARCHAR(20) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    consumed_at DATE NULL,
    discarded_at DATE NULL,
    note TEXT NULL,
    source_type VARCHAR(20) NOT NULL,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_ingredient_name_not_blank
        CHECK (btrim(name) <> ''),
    CONSTRAINT ck_ingredient_expiry_source_type
        CHECK (expiry_source_type IN ('MANUAL', 'POLICY', 'OCR', 'UNKNOWN')),
    CONSTRAINT ck_ingredient_status
        CHECK (status IN ('ACTIVE', 'CONSUMED', 'DISCARDED')),
    CONSTRAINT ck_ingredient_source_type
        CHECK (source_type IN ('MANUAL', 'PHOTO', 'RECEIPT')),
    CONSTRAINT ck_ingredient_expires_at_after_registered_at
        CHECK (
            registered_at IS NULL
            OR expires_at IS NULL
            OR expires_at >= registered_at
        ),
    CONSTRAINT ck_ingredient_status_dates_consistency
        CHECK (
            (status = 'ACTIVE' AND consumed_at IS NULL AND discarded_at IS NULL)
            OR (status = 'CONSUMED' AND consumed_at IS NOT NULL AND discarded_at IS NULL)
            OR (status = 'DISCARDED' AND discarded_at IS NOT NULL AND consumed_at IS NULL)
        ),
    CONSTRAINT fk_ingredient_user
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT fk_ingredient_storage_owner
        FOREIGN KEY (storage_id, user_id) REFERENCES storage(id, user_id) ON DELETE CASCADE,
    CONSTRAINT fk_ingredient_catalog
        FOREIGN KEY (catalog_id) REFERENCES ingredient_catalog(id) ON DELETE SET NULL
);

