CREATE TABLE storage (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL,
    storage_type VARCHAR(20) NOT NULL,
    name VARCHAR(100) NOT NULL,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_storage_type
        CHECK (storage_type IN ('FRIDGE', 'FREEZER', 'PANTRY')),
    CONSTRAINT ck_storage_name_not_blank
        CHECK (btrim(name) <> ''),
    CONSTRAINT fk_storage_user
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT uk_storage_user_storage_type
        UNIQUE (user_id, storage_type),
    CONSTRAINT uk_storage_id_user
        UNIQUE (id, user_id)
);

CREATE TABLE image_asset (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NULL,
    asset_type VARCHAR(30) NOT NULL,
    kind VARCHAR(30) NOT NULL,
    storage_provider VARCHAR(20) NOT NULL,
    image_url TEXT NOT NULL,
    width INT NULL,
    height INT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ck_image_asset_asset_type
        CHECK (asset_type IN ('USER_UPLOAD', 'SYSTEM_DEFAULT')),
    CONSTRAINT ck_image_asset_kind
        CHECK (kind = 'INGREDIENT'),
    CONSTRAINT ck_image_asset_storage_provider
        CHECK (storage_provider IN ('LOCAL', 'S3')),
    CONSTRAINT ck_image_asset_image_url_not_blank
        CHECK (btrim(image_url) <> ''),
    CONSTRAINT ck_image_asset_width_positive
        CHECK (width IS NULL OR width > 0),
    CONSTRAINT ck_image_asset_height_positive
        CHECK (height IS NULL OR height > 0),
    CONSTRAINT ck_image_asset_owner_consistency
        CHECK (
            (asset_type = 'SYSTEM_DEFAULT' AND user_id IS NULL)
            OR (asset_type = 'USER_UPLOAD' AND user_id IS NOT NULL)
        ),
    CONSTRAINT fk_image_asset_user
        FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
);

